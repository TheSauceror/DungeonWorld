	<div id=body>
		<h1>Gameplay in Dungeons of Eld</h1>
		<b>Character Creation: </b>
The first step in your journey is to create your hero. Right now there are 4 playable races and 5 professions to choose from. Each race has advantages and disadvantages to your health and mana stats. The professions will affect stat scaling, which skills are available to your hero, and also have an impact on health and mana.
		<BR><BR>
		<b>Dungeons: </b>
The lands of Eld are home to many dangerous areas. Your job as a hero is to clear the enemy from these strongholds. Alongside the danger however there are riches to be found. Powerful weapons and armor can be obtained from inside these dungeons and the stronger your gear, the stronger your character will become. You can either brave them alone or with a party of heroes. But be warned, the more dungeons you conquer, the harder the enemies you encounter will become.
		<BR><BR>
		<b>Combat: </b>
Combat in Dungeons of Eld is handled entirely by the game. The player has no direct control over the actions of their hero once they enter a dungeon. They do however have the ability to determine their actions prior to entering a dungeon. The battle plan is a set of instructions created by the user which the hero will follow in their encounter. Proper planning will be paramount to success, while a poor battle plan is a recipe for death.
		<BR><BR>
		<b>Character Growth: </b>
Over the countless battle you will encounter your hero will grow stronger. Each monster you defeat drops gold which can be used to level up stats, skills, or buy gear from the marketplace. There is also a chance for a defeated monster to drop gear, which will have random stats assigned to it. Buying the right stats and equipping the best gear will allow your hero to tackle bigger and more dangerous dungeons.
		<BR><BR>
		<b>Social Features: </b>
The main design philosophy behind this game is at its core it should be a social experience. In order to accomplish this we have incorporated several features that are unique to idle RPGs, but work well with our game. Firstly the economy is all player driven. Pieces of loot dropped from dungeons can either be equipped or sold on the global marketplace. There is also a built in party finder where you can take on dungeons with friends or random people from around the globe.
