		<h1>Dungeons of Eld - A Social Idle RPG</h1>
It is the Fourth Era of Eld. The Demon Lord's campaign against the world of man has ended in defeat. The Great Heroes, the strongest fighters from all corners of the world, were able to slay the Demon Lord and send his forces back to the shadows they crawled from. However all is not well, for there is still in danger. Wicked men who take advantage of the towns ravaged by the armies of Hell, fearsome beasts that prey on the flesh of men, and those still loyal to the Demon Lord continue to spread chaos across the land. It is up to you to take up your weapon and forever purge the world of these abominations. But fear not Hero, for you are not alone. With your are the scores of men and women inspired by the bravery of the Great Heros. With your combined strength you seek to carve your name in legend and forever free the land from the binds of the Demon Lord.
		<BR><BR>
Dungeons of Eld is a unique take on the idle RPG genre. With a strong focus on social features and player agency it is more akin to an MMO than other games of the same type. Even with this we managed to stay true to the idle RPG experience with a passive game style and a "play at your own pace" design. Whether it is your first time playing an idle RPG or you are a veteran of the series there is something here for everyone to enjoy. Clicking the banner will take you to the most up to date version of the game. However elements of the game are still subject to change without warning until the game is in a full release state
